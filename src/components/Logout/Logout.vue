<template>
  <div>
    <button @click="logout">Log out <v-icon left>mdi-login</v-icon>
    </button>

  </div>
</template>
<script lang="ts">
// Composition API
import { useAuth0 } from '@auth0/auth0-vue';

export default {
  setup() {
    const auth0 = useAuth0();

    return {
      logout() {
        auth0.logout({
          logoutParams: {
            returnTo: window.location.origin,
          }
        });
      },
    };
  },
};
</script>